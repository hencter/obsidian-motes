/**
 * v2.0.0: 多语言支持。
 *
 * 设计原则：
 *   - 不引入任何运行时依赖（i18next / react-intl 等都不用）
 *   - key-value 直接映射，运行时零开销
 *   - 默认中文 zh-CN（历史兼容，不改变中文用户现有体验）
 *   - 英文 en-US 作为国际版基线
 *   - 未来加语言只需新增一个 dict 对象 + 在 getDict 里加一行
 *
 * 用法：
 *   import { t } from "./i18n";
 *   t("sidebar.todo")          // "待办" / "To-do"
 *   t("notice.saved")          // "已记下" / "Saved"
 *   t("count.memos", { n: 3 }) // 带插值："3 条笔记" / "3 memos"
 */

