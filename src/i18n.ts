let currentDict: Dict = zhCN;
let currentLocale: LocaleKey = "zh-CN";

/** 由外部（main.ts）在插件加载时调用一次，根据 settings.language 决定用哪个字典 */
export function initLocale(setting: LocaleKey): void {
  currentLocale = setting;
  const resolved = resolveLocale(setting);
  currentDict = resolved === "en-US" ? enUS : zhCN;
}

/** 把 "auto" 映射到具体语言：检测 Obsidian 的 moment.locale() 或 activeDocument lang */
function resolveLocale(setting: LocaleKey): "zh-CN" | "en-US" {
  if (setting === "zh-CN") return "zh-CN";
  if (setting === "en-US") return "en-US";
  // auto：检测运行环境
  try {
    // Obsidian 自带 moment，它的 locale() 返回类似 "zh-cn" / "en" / "en-gb"
    const w = window as unknown as { moment?: { locale: () => string } };
    const ml = w.moment?.locale?.().toLowerCase() ?? "";
    if (ml.startsWith("zh")) return "zh-CN";
    if (ml.startsWith("en")) return "en-US";
  } catch {
    /* moment 不可用 */
  }
  try {
    const lang = activeDocument.documentElement.lang.toLowerCase();
    if (lang.startsWith("zh")) return "zh-CN";
    if (lang.startsWith("en")) return "en-US";
  } catch {
    /* activeDocument 不可用 */
  }
  // 默认中文（因为 Motes 最初是中文插件，保持兼容）
  return "zh-CN";
}

/** 翻译函数：t("key") 或 t("key", { n: 3, name: "foo" }) */
export function t(key: string, params?: Record<string, string | number>): string {
  let text = currentDict[key];
  if (text === undefined) {
    // 回退到中文字典
    text = zhCN[key];
    if (text === undefined) {
      // 找不到：返回 key 本身，方便开发时看到漏翻
      return key;
    }
  }
  if (params) {
    for (const [k, v] of Object.entries(params)) {
      // v2.0.19: 用 replacer 函数而不是字符串，避免参数值里的 `$&` / `$1` 等
      //   被 replace 当回溯引用解释出乱码（例如 err.message 里碰巧含 $）。
      const replacement = String(v);
      text = text.replace(new RegExp(`\\{${k}\\}`, "g"), () => replacement);
    }
  }
  return text;
}

/** 返回当前生效的 locale（auto 会返回具体值） */
export function getCurrentLocale(): "zh-CN" | "en-US" {
  return resolveLocale(currentLocale);
}



